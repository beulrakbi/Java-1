package variable;

public class Var2 {
    public static void main(String[] args) {
        /*
        * a라는 그릇을 만들고 그릇의 값을 확인하는 느낌*/

        int a; //변수 선언
        a = 10; //변수 초기화

        System.out.println(a);
        System.out.println(a);
        System.out.println(a);

        /*
        * 10 -> 20으로 변경하시오 */
        System.out.println("변경 후");
        a = 20;
        System.out.println(a);
        System.out.println(a);
        System.out.println(a);
    }
}
