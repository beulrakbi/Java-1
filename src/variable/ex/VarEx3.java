package variable.ex;

public class VarEx3 {
    public static void main(String[] args) {
        /*
        * long 타입의 변수를 선언하고, 그 변수를 100000000000으로 초기화한 후 출력하는 프로그램 작성
        * boolean 타입의 변수를 선언하고, 그 변수를 true로 초기화한 후 출력하는 프로그램을 작성
        * */

        long l = 100000000000L;
        System.out.println("l = " + l);

        boolean b = true;
        System.out.println("b = " + b);
    }
}
