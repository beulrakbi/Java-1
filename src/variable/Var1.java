package variable; //본인이 소속된 패키지 선언

public class Var1 {
    public static void main(String[] args) {
        System.out.println("초기 상태");
        System.out.println(10);
        System.out.println(10);
        System.out.println(10);

        // -> 숫자 10을 20으로 변경하시오

        System.out.println("변경 후");
        /*
        * 수동으로 20으로 변경해야 한다.*/
        System.out.println(20);
        System.out.println(20);
        System.out.println(20);
    }
}
